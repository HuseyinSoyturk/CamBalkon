export class Customer {

    id : number ;
    name : string ;
    adres : string ;
    tel : string ;
    fax : string ;

    constructor(id , name , adres , tel , fax){
        this.id = id;
        this.name = name;
        this.adres = adres;
        this.tel = tel;
        this.fax = fax;
    }

}